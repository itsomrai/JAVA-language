public class loop {
    public static void main(String[] args) {
        for(int i=1;i<20;i++){
            System.out.println(i);
        }
        for(int j=20;j>=1;j--){
            System.out.println(j);
        }

        //While loop
        int k=100;
        while(k>=1){
            System.out.println(k);
            k--;
        }
    }
}
